<?php

$x = 10;  
$y = 6;

#ARITHMETIC OPERATORS

var_dump($x + $y);
echo "<br><br>";

var_dump($x - $y);
echo "<br><br>";

var_dump($x * $y);
echo "<br><br>";

var_dump($x % $y);
echo "<br><br>";

#COMPARISON OPERATORS

#Equal

var_dump($x == $y);
echo "<br><br>";

#Not equal

var_dump($x != $y);
echo "<br><br>";

#Less than

var_dump($x < $y);
echo "<br><br>";

#Grater than

var_dump($x > $y);
echo "<br><br>";

#Less than or equal to

var_dump($x <= $y);
echo "<br><br>";

#Grater than or equal to

var_dump($x >= $y);
echo "<br><br>";

#LOGICAL OPERATORS

#And - True if both $x and $y are true

var_dump($x && $y);
echo "<br><br>";

#And - True if both $x and $y are true

var_dump($x and $y);
echo "<br><br>";

#Or - True if either $x or $y is true

var_dump($x || $y);
echo "<br><br>";

#Or - True if either $x or $y is true

var_dump($x or $y);
echo "<br><br>";

#Not - True if $x is not true

var_dump($x ! $y);
echo "<br><br>";

#Xor - True if either $x or $y is true, but not both

var_dump($x xor $y);
echo "<br><br>";

?>

