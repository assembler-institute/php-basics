<?php
//Absolute value
$absValue = abs(5.5);
echo nl2br($absValue."\n");
//Round Value
$roundValue = round(5.5);
echo nl2br($roundValue."\n");
//The max value of the number series
$array = array(5,7,1,9,3,50);
$maxValue = max($array);
echo nl2br($maxValue."\n");
//The min value of the number series
$minValue = min($array);
echo nl2br($minValue."\n");
//Random value
$randomValue = rand(0,100);
echo nl2br($randomValue."\n");
?>